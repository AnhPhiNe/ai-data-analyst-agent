def render_dashboard_tab(
    profile: dict[str, object],
    auto_analysis: dict[str, object],
    suggestions: dict[str, object],
) -> None:
    ai_status = auto_analysis.get("ai_status", {})
    if ai_status:
        if ai_status.get("used"):
            st.markdown(
                "✨ **AI Analysis Active**: Insights generated by the LLM planner."
            )
        elif ai_status.get("error"):
            st.warning(
                f"⚠️ **AI Fallback Mode**: {ai_status.get('error')} Using built-in statistics instead."
            )
        elif ai_status.get("mode") != "deterministic":
            st.info(
                "ℹ️ **Offline Mode**: AI key not provided. Using built-in statistics."
            )

    overview = auto_analysis.get("overview", {})
    quality = auto_analysis.get("data_quality", {})

    rows = overview.get("rows", profile.get("rows", 0))
    columns = overview.get("columns", profile.get("columns", 0))
    missing_cells = quality.get("total_missing_cells", 0)
    chart_count = len(auto_analysis.get("recommended_charts", []))

    # ── Metric Cards ──────────────────────────────────────────────────────
    st.markdown(
        f"""
        <div class="metric-container">
            <div class="custom-card">
                <div class="card-label">📁 Rows</div>
                <div class="card-value">{rows:,}</div>
            </div>
            <div class="custom-card">
                <div class="card-label">📊 Columns</div>
                <div class="card-value">{columns}</div>
            </div>
            <div class="custom-card">
                <div class="card-label">⚠️ Missing Cells</div>
                <div class="card-value">{missing_cells:,}</div>
            </div>
            <div class="custom-card">
                <div class="card-label">📈 Recommended Charts</div>
                <div class="card-value">{chart_count}</div>
            </div>
        </div>
        """,
        unsafe_allow_html=True,
    )

    # ── Visual Analysis (Charts) — DISPLAYED FIRST as user requested ──────
    charts = auto_analysis.get("recommended_charts", [])
    dataset_frame = st.session_state.get("dataset_frame")
    if charts and isinstance(dataset_frame, pd.DataFrame):
        st.markdown("### 📊 Visual Analysis")
        for i in range(0, len(charts[:4]), 2):
            chart_columns = st.columns(2)
            for j in range(2):
                if i + j < len(charts[:4]):
                    item = charts[i + j]
                    with chart_columns[j]:
                        spec = item.get("chart_spec")
                        if isinstance(spec, dict):
                            spec_copy = dict(spec)
                            if "title" in item:
                                spec_copy["title"] = item.get("title")
                            render_chart_spec(
                                dataset_frame,
                                spec_copy,
                                chart_key=f"dashboard-main-chart-{i+j}",
                            )
                        st.caption(str(item.get("reason", "")))

    # ── Key Insights + Data Quality ───────────────────────────────────────
    story_col, quality_col = st.columns([1.25, 1])
    with story_col:
        st.markdown("### 💡 Key Insights")
        insight_items = _dashboard_insight_items(auto_analysis, suggestions)
        if not insight_items:
            st.info("No insight highlights available yet.")
        else:
            for insight in insight_items:
                st.markdown(
                    f"""
                    <div class="insight-item">
                        <span class="insight-icon">✨</span>
                        <div class="insight-text">{insight}</div>
                    </div>
                    """,
                    unsafe_allow_html=True,
                )

    with quality_col:
        st.markdown("### 🔍 Data Quality")
        missing = pd.DataFrame(quality.get("top_missing_columns", []))
        if missing.empty:
            st.markdown(
                """
                <div class="custom-alert-success">
                    <span style="font-size: 20px;">✨</span>
                    <span>No missing values detected. All columns are fully populated!</span>
                </div>
                """,
                unsafe_allow_html=True,
            )
        else:
            st.dataframe(
                missing[["name", "missing_count", "missing_percent"]],
                use_container_width=True,
                hide_index=True,
            )

    # ── How the dashboard was generated ───────────────────────────────────
    with st.expander("How the dashboard was generated", expanded=False):
        _render_dataset_analysis_activity(auto_analysis, profile)


