def _dashboard_insight_items(
    auto_analysis: dict[str, object], suggestions: dict[str, object]
) -> list[str]:
    items: list[str] = []
    gemini_insights = suggestions.get("insights", [])

    if gemini_insights:
        # If we have AI-generated insights, use them exclusively
        items.extend(str(item) for item in gemini_insights[:6])
    else:
        # Fallback to mechanical insights if AI failed
        numeric = auto_analysis.get("numeric_highlights", [])
        if numeric:
            item = numeric[0]
            items.append(
                f"{item.get('column')} has the widest numeric range: "
                f"{item.get('min')} to {item.get('max')}."
            )

        categorical = auto_analysis.get("categorical_highlights", [])
        if categorical:
            item = categorical[0]
            items.append(
                f"{item.get('column')} is led by \"{item.get('top_value')}\" "
                f"at about {item.get('percent')}% of rows."
            )

        correlations = auto_analysis.get("correlation_highlights", [])
        if correlations:
            item = correlations[0]
            items.append(
                f"Strongest numeric relationship found: {item.get('column_a')} vs "
                f"{item.get('column_b')} (r={item.get('correlation')})."
            )

    return _dedupe_texts(items)[:6]


def _render_dataset_analysis_activity(
    auto_analysis: dict[str, object], profile: dict[str, object]
) -> None:
    overview = auto_analysis.get("overview", {})
    quality = auto_analysis.get("data_quality", {})
    numeric = auto_analysis.get("numeric_highlights", [])
    categorical = auto_analysis.get("categorical_highlights", [])
    correlations = auto_analysis.get("correlation_highlights", [])
    charts = auto_analysis.get("recommended_charts", [])

    rows = overview.get("rows", profile.get("rows", 0))
    columns = overview.get("columns", profile.get("columns", 0))
    missing_cells = quality.get("total_missing_cells", 0)
    missing_columns = quality.get("columns_with_missing", 0)

    steps = [
        f"Loaded {rows} rows and {columns} columns.",
        f"Checked missing values: {missing_cells} missing cells across {missing_columns} columns.",
        f"Summarized {len(numeric)} numeric columns for spread, outliers, and range.",
        f"Ranked {len(categorical)} categorical columns by dominant values.",
    ]
    if correlations:
        strongest = correlations[0]
        steps.append(
            "Found strongest numeric relationship: "
            f"{strongest.get('column_a')} vs {strongest.get('column_b')} "
            f"(r={strongest.get('correlation')})."
        )
    else:
        steps.append("Checked numeric relationships; no strong pair was available.")
    steps.append(f"Prepared {len(charts)} chart recommendations for this dataset.")

    for index, step in enumerate(steps, start=1):
        st.markdown(f"**{index}. {step}**")


def render_agent_activity(tool_trace: list[dict[str, object]]) -> None:
    if not tool_trace:
        return
    st.write("Agent activity")
    for index, trace in enumerate(tool_trace, start=1):
        st.markdown(f"**{index}. {_trace_title(trace)}**")
        detail = _trace_detail(trace)
        if detail:
            st.caption(detail)


def _trace_title(trace: dict[str, object]) -> str:
    source = str(trace.get("source", "agent"))
    status = str(trace.get("status", ""))
    tool_name = trace.get("tool_name")
    if source == "guardrails":
        return "Checked request safety"
    if source == "router":
        if tool_name:
            return f"Router selected `{tool_name}`"
        return "Router evaluated the question"
    if source in {"llm", "gemini"}:
        if status == "skipped":
            return "Skipped LLM fallback"
        if tool_name:
            return f"LLM planner selected `{tool_name}`"
        return "LLM fallback evaluated the request"
    if source == "tool_validation":
        return f"Validated `{tool_name}` arguments"
    if source == "tool_executor":
        return f"Executed `{tool_name}`"
    if source == "agent_column_resolver":
        return "Resolved column names"
    if source == "agent_repair":
        return "Repaired tool arguments"
    if source == "agent_validation":
        return "Checked agent-specific constraints"
    if source == "memory":
        return "Used clarification memory"
    return source.replace("_", " ").title()


def _trace_detail(trace: dict[str, object]) -> str:
    message = str(trace.get("message") or "")
    confidence = trace.get("confidence")
    arguments = trace.get("arguments")
    parts = []
    if message:
        parts.append(message)
    if confidence is not None:
        parts.append(f"confidence={confidence}")
    if isinstance(arguments, dict) and arguments:
        compact_args = ", ".join(
            f"{key}={value}" for key, value in list(arguments.items())[:4]
        )
        parts.append(f"args: {compact_args}")
    return " | ".join(parts)


def _dedupe_texts(items: list[str]) -> list[str]:
    seen = set()
    result = []
    for item in items:
        cleaned = item.strip()
        key = cleaned.lower()
        if not cleaned or key in seen:
            continue
        seen.add(key)
        result.append(cleaned)
    return result


def render_chat_artifacts(
    message: dict[str, object], message_index: int | None = None
) -> None:
    message_id = message.get("id") or message_index or "current"
    if message.get("table"):
        st.dataframe(pd.DataFrame(message["table"]), use_container_width=True)
    if message.get("chart_spec"):
        dataset_frame = st.session_state.get("dataset_frame")
        if isinstance(dataset_frame, pd.DataFrame):
            render_chart_spec(
                dataset_frame,
                message["chart_spec"],
                chart_key=f"chat-chart-{message_id}",
            )
        else:
            st.json(message["chart_spec"])
    if message.get("tool_trace"):
        with st.expander("Agent activity", expanded=True):
            render_agent_activity(message["tool_trace"])
            if st.checkbox("Show raw tool trace", key=f"raw-tool-trace-{message_id}"):
                st.json(message["tool_trace"])
