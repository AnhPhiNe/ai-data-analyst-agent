def render_data_explorer_tab(profile: dict[str, object]) -> None:
    preview_tab, schema_tab, stats_tab, dist_tab = st.tabs(
        [
            "📋 Preview",
            "🏗️ Schema",
            "📈 Statistics",
            "📊 Distributions",
        ]
    )

    with preview_tab:
        preview_data = profile.get("preview", [])
        if preview_data:
            st.dataframe(
                pd.DataFrame(preview_data),
                use_container_width=True,
                hide_index=True,
            )
        else:
            st.info("No preview data available.")

    with schema_tab:
        dtypes = profile.get("dtypes", [])
        if dtypes:
            st.dataframe(
                pd.DataFrame(dtypes),
                use_container_width=True,
                hide_index=True,
            )
        else:
            st.info("No schema information available.")

    with stats_tab:
        # ── Numeric Summary ───────────────────────────────────────────
        st.markdown(
            '<div class="stat-section-title">🔢 Numeric Summary</div>',
            unsafe_allow_html=True,
        )
        numeric_summary = pd.DataFrame(profile.get("numeric_summary", []))
        if numeric_summary.empty:
            st.info("No numeric columns detected.")
        else:
            st.dataframe(numeric_summary, use_container_width=True, hide_index=True)

        # ── Categorical Highlights ────────────────────────────────────
        st.markdown(
            '<div class="stat-section-title">🏷️ Categorical Highlights</div>',
            unsafe_allow_html=True,
        )
        top_categories = profile.get("top_categories", [])
        if not top_categories:
            st.info("No categorical columns detected.")
        else:
            for item in top_categories:
                st.write(f"Top values for `{item['column']}`")
                st.dataframe(
                    pd.DataFrame(item["values"]),
                    use_container_width=True,
                    hide_index=True,
                )

        # ── Missing Values ────────────────────────────────────────────
        st.markdown(
            '<div class="stat-section-title">⚠️ Missing Values</div>',
            unsafe_allow_html=True,
        )
        missing = pd.DataFrame(profile.get("missing_values", []))
        if missing.empty:
            st.success("No missing values detected.")
        else:
            st.dataframe(missing, use_container_width=True, hide_index=True)

    with dist_tab:
        distributions = profile.get("distributions", [])
        if not distributions:
            st.info("No distribution charts available.")
        else:
            # 2-column grid for distributions
            for i in range(0, len(distributions), 2):
                dist_cols = st.columns(2)
                for j in range(2):
                    if i + j < len(distributions):
                        spec = distributions[i + j]
                        with dist_cols[j]:
                            st.write(f"`{spec['column']}`")
                            render_distribution_chart(
                                spec,
                                chart_key=f"explorer-dist-{i+j}-{spec['column']}",
                            )


