def render_chat_tab(
    auto_analysis: dict[str, object],
    suggestions: dict[str, object],
) -> None:
    # ── Suggested Questions (moved here from old "Ask next" tab) ──────────
    questions = _dedupe_texts(
        [str(item) for item in auto_analysis.get("next_questions", [])]
        + [str(item) for item in suggestions.get("questions", [])]
    )
    if questions:
        st.markdown("#### 💡 Suggested Questions")
        question_cols = st.columns(2)
        for index, question_item in enumerate(questions[:6]):
            with question_cols[index % 2]:
                if st.button(
                    question_item,
                    key=f"chat-question-{index}",
                    use_container_width=True,
                ):
                    st.session_state.pending_chat_question = question_item
                    st.rerun()
        st.markdown("---")

    # ── Chat Messages ─────────────────────────────────────────────────────
    if "chat_messages" not in st.session_state:
        st.session_state.chat_messages = []

    for index, message in enumerate(st.session_state.chat_messages):
        with st.chat_message(message["role"]):
            st.write(message["content"])
            render_chat_artifacts(message, message_index=index)

    # ── Chat Input ────────────────────────────────────────────────────────
    pending_question = st.session_state.pop("pending_chat_question", None)
    typed_question = st.chat_input("Ask a question about the uploaded dataset")
    question = pending_question or typed_question

    if question:
        user_message = {
            "id": len(st.session_state.chat_messages),
            "role": "user",
            "content": question,
        }
        st.session_state.chat_messages.append(user_message)
        with st.chat_message("user"):
            st.write(question)

        with st.status(
            "Agent is working on your question...", expanded=True
        ) as status_box:
            try:
                chat_response = None
                for event in stream_chat_question(
                    st.session_state.session_id, question
                ):
                    event_type = event.get("type")
                    if event_type == "step":
                        st.write(str(event.get("message", "Agent step completed.")))
                    elif event_type == "trace":
                        trace = event.get("trace", {})
                        if isinstance(trace, dict):
                            st.write(f"Completed: {_trace_title(trace)}")
                    elif event_type == "final":
                        chat_response = event.get("response")
                    elif event_type == "error":
                        raise RuntimeError(
                            str(event.get("message", "Chat request failed."))
                        )
                if not isinstance(chat_response, dict):
                    raise RuntimeError("Chat stream ended without a final response.")
            except httpx.HTTPStatusError as exc:
                content = exc.response.json().get("detail", "Chat request failed.")
                status_box.update(
                    label="Question analysis failed.",
                    state="error",
                    expanded=True,
                )
                assistant_message = {
                    "id": len(st.session_state.chat_messages),
                    "role": "assistant",
                    "content": content,
                }
            except httpx.RequestError:
                status_box.update(
                    label="Question analysis failed.",
                    state="error",
                    expanded=True,
                )
                assistant_message = {
                    "id": len(st.session_state.chat_messages),
                    "role": "assistant",
                    "content": (
                        "Could not reach the backend. If this is a hosted demo, "
                        "the API may be waking up. Please wait a moment and retry."
                    ),
                }
            except RuntimeError as exc:
                status_box.update(
                    label="Question analysis failed.",
                    state="error",
                    expanded=True,
                )
                assistant_message = {
                    "id": len(st.session_state.chat_messages),
                    "role": "assistant",
                    "content": str(exc),
                }
            else:
                tool_trace = chat_response.get("tool_trace") or []
                if not tool_trace:
                    st.write(
                        "Completed response composition without a pandas tool call."
                    )
                status_box.update(
                    label="Question analysis completed.",
                    state="complete",
                    expanded=False,
                )
                assistant_message = {
                    "id": len(st.session_state.chat_messages),
                    "role": "assistant",
                    "content": chat_response["answer"],
                    "table": chat_response.get("table"),
                    "chart_spec": chat_response.get("chart_spec"),
                    "tool_trace": tool_trace,
                    "clarification_options": chat_response.get("clarification_options"),
                }

        st.session_state.chat_messages.append(assistant_message)
        with st.chat_message("assistant"):
            st.write(assistant_message["content"])
            render_chat_artifacts(
                assistant_message, message_index=int(assistant_message["id"])
            )


